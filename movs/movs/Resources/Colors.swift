import Foundation
import UIKit

extension UIColor{
    
    class var lightOrange: UIColor {
        return UIColor(red:0.97, green:0.81, blue:0.36, alpha:1.0)
    }
    
    class var darkNavy: UIColor {
        return UIColor(red:0.18, green:0.19, blue:0.28, alpha:1.0)
    }
    
    class var buttercup: UIColor {
        return UIColor(red:0.85, green:0.59, blue:0.12, alpha:1.0)
    }
}
