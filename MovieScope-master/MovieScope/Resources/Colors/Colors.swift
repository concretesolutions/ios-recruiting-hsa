import Foundation
import UIKit

//Archivo de colores usados por la app.
extension UIColor{
    
    class var appBackgroundColor: UIColor{
        return UIColor(red:0.11, green:0.11, blue:0.15, alpha:1.0)
    }
    
    class var imageLoadingBackgroundColor:UIColor{
         return UIColor(red:0.34, green:0.30, blue:0.30, alpha:1.0)
    }
    
    class var lightGray: UIColor{
        return UIColor(red:0.65, green:0.68, blue:0.75, alpha:1.0)
    }
}
